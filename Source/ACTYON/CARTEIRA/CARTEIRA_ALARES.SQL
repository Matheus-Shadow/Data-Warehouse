SELECT 
    CAST(GETDATE() AS DATE) AS DATA,
    CAST(T.CONT_ID AS INT) AS ID_CRM,
    CAST(
        CONCAT(
            RTRIM(CAST(D.CPF AS VARCHAR(20))), 
            RTRIM(CAST(D.DEVEDOR_ID AS VARCHAR(19)))
        ) AS VARCHAR(50)
    ) AS ID_CLI,
    CAST(D.CPF AS VARCHAR(20)) AS CPF,
    CAST(T.DEVEDOR_ID AS BIGINT) AS DEVEDOR_ID,
    CAST(D.IDENTIFICADOR_ID AS varchar(20)) AS IDENTIFICADOR_ID,
    CAST(T.TITULO_ID AS BIGINT) AS TITULO_ID,
    CAST(T.NUMERO_CONTRATO AS VARCHAR(55)) AS CONTRATO_ID,
    CAST(T.NUMERO_DOCUMENTO AS VARCHAR(65)) AS DOCUMENTO_ID,
    CAST(D.NOME AS VARCHAR(200)) AS NOME,
    CAST(D.UF AS CHAR(2)) AS UF,
    CAST(D.CIDADE AS VARCHAR(100)) AS CIDADE,
    CAST(D.BAIRRO AS VARCHAR(150)) AS BAIRRO,
    CAST(D.CEP AS VARCHAR(15)) AS CEP,
    CAST(D.SEXO AS CHAR(1)) AS SEXO,
    CAST(D.DATA_NASCIMENTO AS DATETIME2) AS DATA_NASCIMENTO,
    CAST(tp.DESCRICAO AS VARCHAR(25)) AS TIPO_TITULO,
    CAST(T.LOJA_ID AS VARCHAR(200)) AS SEGMENTO,
    CAST(T.NUMERO_PRESTACAO AS INT) AS NUMERO_PRESTACAO,
    CAST(T.QTDE_PRESTACAO AS INT) AS QTD_PRESTACAO,
    CAST(T.NUMERO_BANCO AS VARCHAR(30)) AS NUMERO_BANCO,
    CAST(T.SETOR AS VARCHAR(20)) AS SETOR,
    CAST(T.VALOR AS NUMERIC(15, 2)) AS VALOR,
    CAST(T.VALOR_ORIGINAL AS NUMERIC(15, 2)) AS VALOR_ORIGINAL,
    CAST(T.LOTE AS VARCHAR(30)) AS LOTE,
    CAST(T.DATA_VENCIMENTO AS DATETIME2) AS DATA_VENCIMENTO,
    CAST(T.DATA_VENCIMENTO_ORIGINAL AS DATETIME2) AS DATA_VENCIMENTO_ORIGINAL,
    CAST(T.DATA_IMPORTACAO AS DATETIME2) AS DATA_IMPORTACAO,
    CAST(T.OBSE AS VARCHAR(720)) AS OBSE,
    CAST(T.DATA_DEVOLUCAO AS DATETIME2) AS DATA_DEVOLUCAO,
    CAST(T.DATA_INCLUSAO AS DATETIME2) AS DATA_INCLUSAO,
    CAST(T.DATA_ALTERACAO AS DATETIME2) AS DATA_ALTERACAO,
    CAST(T.USUARIO_INCLUSAO AS VARCHAR(20)) AS USUARIO_INCLUSAO,
    CAST(T.USUARIO_ALTERACAO AS VARCHAR(20)) AS USUARIO_ALTERACAO,
    CAST(T.BORDERO_ID AS VARCHAR(75)) AS IMP_ID,
    CAST(T.ACORDO_ID AS VARCHAR(10)) AS ACORDO_ID,
    CAST(T.TIPO_PRODUTO AS VARCHAR(55)) AS TIPO_PRODUTO,
    CAST('45.77.193.154' AS VARCHAR(20)) AS INSTANCIA,
    CAST('dbActyon_MEF' AS VARCHAR(20)) AS BANCO,
    CAST('ACTYON' AS VARCHAR(30)) AS ORIGEM

FROM tbtitulo T WITH (NOLOCK)
LEFT JOIN tbdevedor D WITH (NOLOCK) ON T.DEVEDOR_ID = D.DEVEDOR_ID
LEFT JOIN tbtipo_titulo TP WITH (NOLOCK) ON T.TIPO_TITULO_ID = TP.TIPO_TITULO_ID
WHERE T.VALOR > 0;