select 
 CAST(GETDATE() AS DATE) as DATA,
 T.CONT_ID AS ID_CRM,
 CONCAT(CAST(D.CPF AS NVARCHAR), CAST(D.DEVEDOR_ID AS NVARCHAR)) AS ID_CLI,
 D.CPF,
 T.DEVEDOR_ID, 
 D.IDENTIFICADOR_ID,
 T.TITULO_ID, 
 T.NUMERO_CONTRATO AS CONTRATO_ID,
 T.NUMERO_DOCUMENTO DOCUMENTO_ID,
 D.NOME,
 D.UF,
 D.CIDADE,
 D.BAIRRO,
 D.CEP,
 D.SEXO,
 D.DATA_NASCIMENTO,
 tp.DESCRICAO AS TIPO_TITULO,
 T.LOJA_ID AS SEGMENTO,
 T.NUMERO_PRESTACAO,
 T.QTDE_PRESTACAO,
 T.NUMERO_BANCO,
 T.SETOR,
 T.VALOR,
 T.VALOR_ORIGINAL,
 T.LOTE,
 T.DATA_VENCIMENTO,
 T.DATA_VENCIMENTO_ORIGINAL, 
 T.DATA_IMPORTACAO,
 T.OBSE,
 T.DATA_DEVOLUCAO,
 T.DATA_INCLUSAO,
 T.DATA_ALTERACAO,
 T.USUARIO_INCLUSAO, 
 T.USUARIO_ALTERACAO,
 T.BORDERO_ID AS IMP_ID,
 T.ACORDO_ID,
 T.TIPO_PRODUTO
from tbtitulo T With(NoLock) 
left join tbdevedor d WITH(NoLock) on t.DEVEDOR_ID = d.DEVEDOR_ID
left join tbtipo_titulo tp WITH(NoLock) on t.TIPO_TITULO_ID = tp.TIPO_TITULO_ID
where T.VALOR > 0