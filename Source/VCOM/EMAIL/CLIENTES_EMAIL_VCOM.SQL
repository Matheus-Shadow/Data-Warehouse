SELECT
    CAST(A.DATA_CAD AS DATETIME2(0)) AS DATA_INCLUSAO,
    CAST(A.DATA_UP AS DATETIME2(0)) AS DATA_ALTERACAO,
    CAST('230' AS INT) AS COD_CRM,
    CAST(B.CPFCGC_PES AS VARCHAR(20)) AS CPF,
    CAST(B.COD_PES AS BIGINT) AS ID_KEY,
    CAST(A.RUA_ENDE AS NVARCHAR(200)) AS EMAIL,
    CAST(C.DES_STATE AS NVARCHAR(120)) AS SITUACAO_EMAIL,
    CAST(D.NOME_TIPE AS NVARCHAR(80)) AS TIPO_EMAIL,
    CAST('' AS NVARCHAR(120)) AS ORIGEM_EMAIL,
    CAST('52.179.19.141' AS VARCHAR(20)) AS INSTANCIA, 
    CAST('MF_cobsystems' AS VARCHAR(20)) AS BANCO, 
    CAST('VCOM' AS VARCHAR(30)) AS ORIGEM
FROM PESSOAS_ENDERECOS A WITH(NOLOCK) 
INNER JOIN PESSOAS B WITH (NOLOCK) ON A.COD_PES = B.COD_PES
INNER JOIN ENDERECOS_STATUS C WITH (NOLOCK) ON A.COD_STATE = C.COD_STATE
INNER JOIN TIPOS_ENDERECOS D WITH (NOLOCK) ON A.COD_TIPE = D.COD_TIPE
WHERE A.COD_TIPE = 6 AND A.DATA_CAD IS NOT NULL
AND 
    (CAST(A.DATA_CAD AS DATE) = CAST(GETDATE() AS DATE)
    OR CAST(A.DATA_UP AS DATE) = CAST(GETDATE() AS DATE))